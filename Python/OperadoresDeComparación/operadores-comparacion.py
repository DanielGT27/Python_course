# Operadores de comparaci√≥n
# == es igual que

es_igual_que = 5 == 6

# != es distinto de

distinto_que = 5 != 6
# < es menor que 

menor_que = 5 < 6
# <= es menor o igual que

menor_o_igual= 5 <= 6

# > es mayor que 

mayor_que = 5 > 6
# >= es mayor o igual que

mayor_o_igual= 5 >= 6


print(es_igual_que) 
print(distinto_que)
print(menor_que)
print(menor_o_igual)
print(mayor_que)
print(mayor_o_igual)

